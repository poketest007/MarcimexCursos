function Script1()
{
  var player = GetPlayer();

var lmsAPI = findLMSAPI(this);
var newName = "Guest";
var newMail = "";

function findLMSAPI(win) {
    if (win.hasOwnProperty("GetStudentID")) return win;
    else if (win.parent == win) return null;
    else return findLMSAPI(win.parent);
}

if (lmsAPI)
{
    var myMail = lmsAPI.GetStudentID();
    var myName = lmsAPI.GetStudentName();
    var arrayName = myName.split(',');
    var arrayCorreo = myMail.split(',');

    if (arrayName.length>1){
        newName = arrayName[1] + ' ' + arrayName[0];
    }else{
        newName = arrayName[0];
    }

    if (arrayCorreo.length>1){
        newMail = arrayCorreo[1] + ' ' + arrayCorreo[0];
    }else{
        newMail = arrayCorreo[0];
    }
}

player.SetVar("name", newName.trim());
player.SetVar("mail", newMail.trim());

}

